    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         String pass = txtPassword2.getText();
         String passCon = txtPassword1.getText();
         String uid = txtUser1.getText();
        
        char arr [] = pass.toCharArray();
        //password validation
       if(upperDon == false)
       {
           lblUpper.setForeground(Color.RED);
       }
       else
       {
          lblUpper.setForeground(Color.WHITE);
       }
       
        //password validation
       if(digitDon == false)
       {
           lblDigit.setForeground(Color.RED);
       }
       else
       {
          lblDigit.setForeground(Color.WHITE);
       }
       
       if(txtPassword2.getText().length() < 8)
       {
       lbl8Char2.setForeground(Color.RED);
       }
       else
       {
       lbl8Char2.setForeground(Color.WHITE);
       }
                  
        try{
        if(txtPassword2.getText().length() > 8 && (txtUser1.getText().isEmpty()) == false && (txtPassword1.getText().isEmpty()) == false && pass.equals(passCon) && upperDon == true && digitDon == true)
        {
          
          
            String query = "select id from userdetails where id = '" + uid + "';";
         
                st = con.createStatement();
                
               ResultSet rs = st.executeQuery(query);
              
               if(rs.next())
               {
                   if(rs.getString("id").equals(uid))
                   {
                        String query1 = "update userdetails set pass = '" + pass + "' where id = '" + uid + "';" ;
                        st.executeUpdate(query1);
                        
                        JOptionPane.showMessageDialog(this,"Password Updated!");
                        
                        Login obj = new Login();
                        obj.show();
                        this.dispose();
                   }
                   else
                   {
                       lblerror.setText("Invalid user id!");
                   }
               }
               else
               {
                   lblerror.setText("Please sign in first!");
               }
                
          
          
        }
        else
        {
            lblerror.setText("all fields are required*");
            
        }
        }
        catch(ArrayIndexOutOfBoundsException ee)
        {
             System.out.println(ee.toString());
        }
        catch(Exception e)
        {
            System.out.println(e.toString());
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

