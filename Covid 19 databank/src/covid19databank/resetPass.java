    private void txtPassword2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPassword2KeyReleased
        // TODO add your handling code here:
          String pass = txtPassword2.getText();
        
        char arr [] = pass.toCharArray();
        //password validation
        for(int i = 0; i < arr.length; i++)
        {
            if(Character.isUpperCase(arr[i]))
            {
                lblUpper.setFont(new Font("Arial",Font.BOLD,12));
                upperDon = true;
            }
           
            
            if(Character.isDigit(arr[i]))
            {
                 lblDigit.setFont(new Font("Arial",Font.BOLD,12));
                 digitDon = true;
            }
          
      
        }
        
        if(arr.length > 8)
        {
            lbl8Char2.setFont(new Font("Arial",Font.BOLD,12));
        }
        
        if(arr.length == 0)
        {
            lbl8Char2.setFont(new Font("Arial",Font.PLAIN,12));
            lblDigit.setFont(new Font("Arial",Font.PLAIN,12));
            lblUpper.setFont(new Font("Arial",Font.PLAIN,12));
        }
    }//GEN-LAST:event_txtPassword2KeyReleased

