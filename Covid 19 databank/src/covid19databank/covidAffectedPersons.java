    public void getData() {

        try {

            //create statement
            st = con.createStatement();

            //count Deaths
            String query1 = "select count(id) from addvacc where covidAffect = 'yes';";

            ResultSet rs1 = st.executeQuery(query1);

            if (rs1.next()) {
                //call vaccine data method 
                vaccinedPData.count();
                double avg = (rs1.getInt("count(id)") / (vaccinedPData.COUNT * 1.0)) * 100.0;
                DecimalFormat df = new DecimalFormat("####0.00");
                lbltotalAffected.setText(Integer.toString(rs1.getInt("count(id)")));
                lblAVGAffected.setText(df.format(avg) + "%");

            }

      
            //get all full vaccined persons details
            String query = "select * from addvacc where covidAffect = 'yes';";

            ResultSet rs = st.executeQuery(query);

            while (rs.next()) {
 
                String name = rs.getString("name");
                String nic = rs.getString("nic");
                String age = rs.getString("age");
                String fullvac = rs.getString("fullVac");
                String tp = Integer.toString(rs.getInt("tp"));
             
                
                String tbData[] = {name,nic,age,tp,fullvac};
                
                DefaultTableModel tblModel = (DefaultTableModel)tblVaccinedPerson.getModel();
                
                tblModel.addRow(tbData);

            }

        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }

