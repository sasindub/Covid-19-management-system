    private void btnReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReportActionPerformed
        // TODO add your handling code here:

        //file saving path
        File file = new File("Covid19AffectedPersons.txt");

        try{
            //make a new file
            FileWriter fw = new FileWriter(file);
            BufferedWriter bw = new BufferedWriter(fw);
            fw.write("\t\t---Covid 19 Affected Persons List----");
            fw.write("\n\n");

            fw.write("\tNAME   \t|   NIC   |\t  AGE \t| FULL VACCINED \t| TELE\n");

            bw.newLine();
            bw.newLine();

            for(int i = 0; i < tblVaccinedPerson.getRowCount(); i++){//rows

                for(int j = 0; j < tblVaccinedPerson.getColumnCount(); j++){//columns

                    bw.write("\t| " + tblVaccinedPerson.getValueAt(i,j).toString() + "\t" );

                }
                bw.newLine();
            }

            bw.close();
            fw.close();

            JOptionPane.showMessageDialog(this, "Report has been genareted!","report",JOptionPane.INFORMATION_MESSAGE);
        }catch(IOException e)
        {
            System.out.println(e.toString());
        }
        catch(Exception ee)
        {
            System.out.println(ee.toString());
        }
    }//GEN-LAST:event_btnReportActionPerformed

