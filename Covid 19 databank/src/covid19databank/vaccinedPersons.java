    public void getData()
    {
     
        try{
        
            //call vaccine data method 
              vaccinedPData.count();
              //get vaccined person count from it
             lbltotalVP.setText("TOTAL VACCINED PERSONS: " + vaccinedPData.COUNT);
            
            //create statement
            st = con.createStatement();
           
            //get all vaccined persons details
            String query = "select * from addvacc;";
            
            ResultSet rs = st.executeQuery(query);
            
            while(rs.next())
            {
                String nic = rs.getString("nic");
                String name = rs.getString("name");
                String age = Integer.toString(rs.getInt("age"));
                String vacc1 = rs.getString("1stVac");
                String vacc2 = rs.getString("2ndVac");
                String booster = rs.getString("booster");
                
                String tbData[] = {nic,name,age,vacc1,vacc2,booster};
                
                DefaultTableModel tblModel = (DefaultTableModel)tblVaccinedPerson.getModel();
                
                tblModel.addRow(tbData);
            }
            
        }
        catch(Exception e)
        {
            System.out.println(e.toString());
        }
    }
            

