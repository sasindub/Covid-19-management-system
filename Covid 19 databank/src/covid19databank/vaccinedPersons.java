    private void btnAffectedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAffectedActionPerformed
        // TODO add your handling code here:
        if(txtAffectedDate.getText().isEmpty() | txtReportCode.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Please enter report code and date!","empty",JOptionPane.ERROR_MESSAGE);
        }
        else{
        
            try{
                int row = tblVaccinedPerson.getSelectedRow();
                
                String report = txtReportCode.getText();
                String day = txtAffectedDate.getText();
                String cell = tblVaccinedPerson.getModel().getValueAt(row,0).toString();
                st = con.createStatement();
                
                String query2 = "select nic from affected where nic = '" + cell + "'";
                
                ResultSet rs = st.executeQuery(query2);
                
                if(rs.next())
                {
                    JOptionPane.showMessageDialog(this, "This person is current covid \naffected patient!","warning",JOptionPane.WARNING_MESSAGE);
                }
                else{
                String query = "insert into affected(nic,reportNo,day) values('" + cell + "','" + report + "','" + day + "');";
                st.executeUpdate(query);
                
                //update addvacc table as covid affected
                String query1 =  "update addvacc set covidAffect = 'yes' where nic = '" + cell + "';";
                st.executeUpdate(query1);
                
                JOptionPane.showMessageDialog(this, "Covid 19 Affected Patient Updated!","add",JOptionPane.INFORMATION_MESSAGE);
                
                }
                
                
                
                
            }catch(Exception e)
            {
                System.out.println(e.toString());
            }
            
        }
    }//GEN-LAST:event_btnAffectedActionPerformed

